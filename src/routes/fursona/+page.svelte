<h2 id="history">History</h2>
<p>
	Hyper's first name was originally Dante. He was mostly inspired from Legoshi and my personality. I
	created him around the end of 2020 for a story I wanted to write. His history and personality were
	created before his appearance was.
</p>
<p>
	I commissioned him December 2021 for his reference sheet. I wanted his design to be simple, as I
	didn't want to think to hard on patterns and whatnot.
</p>
<p>He's turned into a copy of myself, a way to experiment different aspects of my life onto him.</p>
<hr />
